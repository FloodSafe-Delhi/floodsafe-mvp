import { useState } from 'react';
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query';
import { Card } from '../ui/card';
import { Badge } from '../ui/badge';
import { Button } from '../ui/button';
import { Switch } from '../ui/switch';
import { Label } from '../ui/label';
import { Separator } from '../ui/separator';
import { Avatar, AvatarFallback } from '../ui/avatar';
import { Progress } from '../ui/progress';
import { MapPin, Award, Bell, Globe, Settings, LogOut, Edit, Trash2, FileText, Route } from 'lucide-react';
import { useUserReports, Report, useDailyRoutes, useDeleteDailyRoute } from '../../lib/api/hooks';
import { Dialog, DialogContent, DialogHeader, DialogTitle, DialogTrigger } from '../ui/dialog';
import { Input } from '../ui/input';
import { RadioGroup, RadioGroupItem } from '../ui/radio-group';
import { Checkbox } from '../ui/checkbox';
import { toast } from 'sonner';
import { User } from '../../types';
import { useAuth } from '../../contexts/AuthContext';
import { API_BASE_URL } from '../../lib/api/config';

// For demo purposes, using a hardcoded user ID
// In production, this would come from auth context
const DEMO_USER_ID = 'admin'; // Will be created by seed script

interface WatchArea {
  id: string;
  name: string;
  latitude: number;
  longitude: number;
  radius: number;
  created_at: string;
}

// Helper function to normalize user data with defaults
const normalizeUserData = (userData: User): User => {
  return {
    ...userData,
    language: userData.language || 'english',
    notification_push: userData.notification_push ?? true,
    notification_sms: userData.notification_sms ?? false,
    notification_whatsapp: userData.notification_whatsapp ?? false,
    notification_email: userData.notification_email ?? true,
    alert_preferences: userData.alert_preferences || {
      watch: true,
      advisory: true,
      warning: true,
      emergency: true,
    },
  };
};

interface ProfileScreenProps {
  onNavigate?: (screen: 'privacy' | 'terms') => void;
}

export function ProfileScreen({ onNavigate }: ProfileScreenProps) {
  const queryClient = useQueryClient();
  const { logout } = useAuth();
  const [editDialogOpen, setEditDialogOpen] = useState(false);

  // Fetch user profile
  const { data: rawUser, isLoading } = useQuery<User>({
    queryKey: ['user', DEMO_USER_ID],
    queryFn: async () => {
      // Try to get admin user first
      const usersResponse = await fetch(`${API_BASE_URL}/leaderboards/top?limit=50`);
      const users = await usersResponse.json();
      const adminUser = users.find((u: User) => u.username === 'admin');

      if (adminUser) {
        return normalizeUserData(adminUser);
      }

      // If no admin user, get first user or show error
      if (users.length > 0) {
        return normalizeUserData(users[0]);
      }

      throw new Error('No users found. Please seed the database.');
    },
  });

  const user = rawUser;

  // Fetch watch areas
  const { data: watchAreas = [] } = useQuery<WatchArea[]>({
    queryKey: ['watchAreas', user?.id],
    queryFn: async () => {
      if (!user?.id) return [];
      const response = await fetch(`${API_BASE_URL}/watch-areas/user/${user.id}`);
      if (!response.ok) return [];
      return response.json();
    },
    enabled: !!user?.id,
  });

  // Fetch user's reports
  const { data: userReports = [], isLoading: reportsLoading } = useUserReports(user?.id);

  // Fetch daily routes
  const { data: dailyRoutes = [] } = useDailyRoutes(user?.id);
  const deleteRouteMutation = useDeleteDailyRoute();

  // Delete watch area mutation
  const deleteWatchAreaMutation = useMutation({
    mutationFn: async (watchAreaId: string) => {
      const response = await fetch(`${API_BASE_URL}/watch-areas/${watchAreaId}`, {
        method: 'DELETE',
      });
      if (!response.ok) throw new Error('Failed to delete watch area');
      return response.json();
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['watchAreas'] });
      toast.success('Watch area deleted');
    },
    onError: () => {
      toast.error('Failed to delete watch area');
    },
  });

  // Update user mutation
  const updateUserMutation = useMutation({
    mutationFn: async (updates: Partial<User>) => {
      const response = await fetch(`${API_BASE_URL}/users/${user?.id}`, {
        method: 'PATCH',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(updates),
      });
      if (!response.ok) throw new Error('Failed to update profile');
      return response.json();
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['user'] });
      toast.success('Profile updated successfully!');
    },
    onError: () => {
      toast.error('Failed to update profile');
    },
  });

  const handleNotificationToggle = (field: string, value: boolean) => {
    if (!user) return;
    updateUserMutation.mutate({ [field]: value } as Partial<User>);
  };

  const handleAlertPreferenceToggle = (alertType: string, value: boolean) => {
    if (!user || !user.alert_preferences) return;
    const newPreferences = { ...user.alert_preferences, [alertType]: value };
    updateUserMutation.mutate({
      alert_preferences: newPreferences
    } as Partial<User>);
  };

  const handleLanguageChange = (language: string) => {
    if (!user) return;
    updateUserMutation.mutate({ language } as Partial<User>);
  };

  if (isLoading) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="text-lg">Loading profile...</div>
      </div>
    );
  }

  if (!user) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="text-center">
          <h2 className="text-xl font-semibold mb-2">No User Found</h2>
          <p className="text-gray-600">Please run the database seed script</p>
        </div>
      </div>
    );
  }

  const getInitials = (name: string | undefined) => {
    if (!name) return '??';
    return name.split(' ').map(n => n[0]).join('').toUpperCase().slice(0, 2);
  };

  const progressToNextLevel = ((user.points % 100) / 100) * 100;
  const pointsNeeded = 100 - (user.points % 100);
  const memberSince = user.created_at
    ? new Date(user.created_at).toLocaleDateString('en-US', { month: 'short', year: 'numeric' })
    : 'Recently';
  const reportsCount = user.reports_count ?? 0;
  const verifiedReportsCount = user.verified_reports_count ?? 0;

  return (
    <div className="pb-16 md:pb-4 min-h-screen bg-gray-50">
      {/* Profile Header */}
      <div className="bg-gradient-to-br from-blue-600 to-blue-700 text-white p-6">
        <div className="flex items-center gap-4 mb-4">
          <Avatar className="w-16 h-16 bg-white text-blue-600">
            <AvatarFallback className="text-2xl font-semibold">
              {getInitials(user.username)}
            </AvatarFallback>
          </Avatar>
          <div className="flex-1">
            <div className="flex items-center gap-2 mb-1">
              <h2 className="text-xl font-semibold">{user.username}</h2>
              <Badge variant="secondary" className="text-xs">
                {user.role}
              </Badge>
            </div>
            <p className="text-sm opacity-90">{user.email}</p>
            {user.phone && <p className="text-xs opacity-75 mt-1">{user.phone}</p>}
            <p className="text-xs opacity-75 mt-1">Joined {memberSince}</p>
          </div>
          <Dialog open={editDialogOpen} onOpenChange={setEditDialogOpen}>
            <DialogTrigger asChild>
              <Button variant="secondary" size="sm">
                <Edit className="w-4 h-4 mr-1" />
                Edit
              </Button>
            </DialogTrigger>
            <EditProfileDialog user={user} onSave={(updates) => {
              updateUserMutation.mutate(updates);
              setEditDialogOpen(false);
            }} />
          </Dialog>
        </div>
      </div>

      <div className="p-4 space-y-4">
        {/* Profile Completion Card */}
        {!user.profile_complete && (
          <Card className="p-4 bg-blue-50 border-blue-200">
            <div className="flex items-center justify-between mb-3">
              <h3 className="font-semibold text-lg flex items-center gap-2">
                <Settings className="w-5 h-5 text-blue-600" />
                Complete Your Profile
              </h3>
              <Badge variant="secondary" className="text-xs">
                Step {user.onboarding_step || 1} of 5
              </Badge>
            </div>

            <div className="space-y-2">
              <div className="flex justify-between text-sm mb-1">
                <span className="text-gray-700">Profile Completion</span>
                <span className="text-blue-600 font-medium">
                  {Math.round(((user.onboarding_step || 0) / 5) * 100)}%
                </span>
              </div>
              <div className="h-2 bg-gray-200 rounded-full overflow-hidden">
                <div
                  className="h-full bg-blue-600 transition-all duration-300"
                  style={{ width: `${((user.onboarding_step || 0) / 5) * 100}%` }}
                />
              </div>

              <p className="text-xs text-gray-600 mt-2">
                Complete your profile to unlock all features and personalized flood alerts
              </p>
            </div>
          </Card>
        )}

        {/* Reputation Section */}
        <Card className="p-4">
          <div className="flex items-center justify-between mb-3">
            <h3 className="font-semibold text-lg flex items-center gap-2">
              <Award className="w-5 h-5 text-yellow-600" />
              Reputation Score
            </h3>
            <div className="text-right">
              <div className="text-2xl font-bold text-blue-600">Level {user.level}</div>
              <div className="text-sm text-gray-600">{user.points} points</div>
            </div>
          </div>

          <div className="space-y-2 mb-4">
            <div className="flex justify-between text-sm">
              <span>Progress to Level {user.level + 1}</span>
              <span className="text-gray-600">{pointsNeeded} more points</span>
            </div>
            <Progress value={progressToNextLevel} className="h-2" />
          </div>

          <div className="grid grid-cols-3 gap-3 text-center">
            <div className="p-3 bg-gray-50 rounded-lg">
              <div className="text-2xl font-bold">{reportsCount}</div>
              <div className="text-xs text-gray-600">Submitted</div>
            </div>
            <div className="p-3 bg-green-50 rounded-lg">
              <div className="text-2xl font-bold text-green-600">{verifiedReportsCount}</div>
              <div className="text-xs text-gray-600">Verified</div>
            </div>
            <div className="p-3 bg-yellow-50 rounded-lg">
              <div className="text-2xl font-bold text-yellow-600">
                {reportsCount - verifiedReportsCount}
              </div>
              <div className="text-xs text-gray-600">Pending</div>
            </div>
          </div>

          {user.badges && user.badges.length > 0 && (
            <div className="mt-4 p-3 bg-blue-50 border border-blue-200 rounded-lg">
              <div className="text-sm font-medium text-blue-900 mb-2">Badges Earned</div>
              <div className="flex flex-wrap gap-2">
                {user.badges.map((badge, idx) => (
                  <Badge key={idx} variant="secondary" className="bg-blue-100 text-blue-800">
                    {badge}
                  </Badge>
                ))}
              </div>
            </div>
          )}
        </Card>

        {/* Watch Areas */}
        <Card className="p-4">
          <div className="flex items-center justify-between mb-3">
            <h3 className="font-semibold text-lg flex items-center gap-2">
              <MapPin className="w-5 h-5 text-gray-600" />
              Watch Areas ({watchAreas.length})
            </h3>
          </div>

          {watchAreas.length > 0 ? (
            <div className="space-y-2">
              {watchAreas.map((area) => (
                <div key={area.id} className="flex items-center justify-between p-3 bg-gray-50 rounded-lg">
                  <div className="flex items-center gap-2 flex-1">
                    <MapPin className="w-4 h-4 text-gray-600" />
                    <div>
                      <div className="text-sm font-medium">{area.name}</div>
                      <div className="text-xs text-gray-500">
                        Radius: {(area.radius / 1000).toFixed(1)}km
                      </div>
                    </div>
                  </div>
                  <Button
                    size="sm"
                    variant="ghost"
                    onClick={() => deleteWatchAreaMutation.mutate(area.id)}
                    disabled={deleteWatchAreaMutation.isPending}
                  >
                    <Trash2 className="w-4 h-4 text-red-500" />
                  </Button>
                </div>
              ))}
            </div>
          ) : (
            <div className="text-center py-6 text-gray-500">
              <MapPin className="w-12 h-12 mx-auto mb-2 opacity-50" />
              <p className="text-sm">No watch areas yet</p>
              <p className="text-xs mt-1">Add locations to monitor for alerts</p>
            </div>
          )}
        </Card>

        {/* City Preference */}
        <Card className="p-4">
          <div className="flex items-center justify-between mb-3">
            <h3 className="font-semibold text-lg flex items-center gap-2">
              <MapPin className="w-5 h-5 text-gray-600" />
              City Preference
            </h3>
          </div>

          <div className="flex items-center justify-between p-3 bg-gray-50 rounded-lg">
            <div>
              <div className="text-sm font-medium">
                {user.city_preference ? (
                  user.city_preference === 'bangalore' ? 'Bangalore' : 'Delhi'
                ) : (
                  'Not set'
                )}
              </div>
              <div className="text-xs text-gray-500">
                Your primary city for flood alerts
              </div>
            </div>
            {user.city_preference && (
              <Badge variant="outline" className="text-xs">
                {user.city_preference === 'bangalore' ? 'Karnataka' : 'NCT'}
              </Badge>
            )}
          </div>

          {!user.city_preference && (
            <p className="text-xs text-gray-500 mt-2">
              Set your city preference during onboarding or in settings
            </p>
          )}
        </Card>

        {/* Daily Routes */}
        <Card className="p-4">
          <div className="flex items-center justify-between mb-3">
            <h3 className="font-semibold text-lg flex items-center gap-2">
              <Route className="w-5 h-5 text-gray-600" />
              Daily Routes ({dailyRoutes.length})
            </h3>
          </div>

          {dailyRoutes.length > 0 ? (
            <div className="space-y-2">
              {dailyRoutes.map((route) => (
                <div key={route.id} className="flex items-center justify-between p-3 bg-gray-50 rounded-lg">
                  <div className="flex items-center gap-2 flex-1">
                    <Route className="w-4 h-4 text-gray-600" />
                    <div className="flex-1">
                      <div className="text-sm font-medium">{route.name}</div>
                      <div className="text-xs text-gray-500 capitalize">
                        {route.transport_mode} ‚Ä¢ Flood alerts: {route.notify_on_flood ? 'On' : 'Off'}
                      </div>
                    </div>
                  </div>
                  <Button
                    size="sm"
                    variant="ghost"
                    onClick={() => {
                      if (confirm(`Delete route "${route.name}"?`)) {
                        deleteRouteMutation.mutate(route.id, {
                          onSuccess: () => toast.success('Route deleted'),
                          onError: () => toast.error('Failed to delete route'),
                        });
                      }
                    }}
                  >
                    <Trash2 className="w-4 h-4 text-red-500" />
                  </Button>
                </div>
              ))}
            </div>
          ) : (
            <div className="text-center py-6 text-gray-500">
              <Route className="w-12 h-12 mx-auto mb-2 opacity-50" />
              <p className="text-sm">No daily routes yet</p>
              <p className="text-xs mt-1">Add routes to get flood alerts along your commute</p>
            </div>
          )}
        </Card>

        {/* My Reports */}
        <Card className="p-4">
          <div className="flex items-center justify-between mb-3">
            <h3 className="font-semibold text-lg flex items-center gap-2">
              <FileText className="w-5 h-5 text-gray-600" />
              My Reports ({userReports.length})
            </h3>
          </div>

          {reportsLoading ? (
            <div className="text-center py-6 text-gray-500">
              <div className="text-sm">Loading reports...</div>
            </div>
          ) : userReports.length > 0 ? (
            <div className="space-y-2 max-h-64 overflow-y-auto">
              {userReports.slice(0, 10).map((report: Report) => (
                <div key={report.id} className="p-3 bg-gray-50 rounded-lg">
                  <div className="flex items-start justify-between">
                    <div className="flex-1">
                      <div className="text-sm font-medium line-clamp-2">{report.description}</div>
                      <div className="flex items-center gap-2 mt-1">
                        <span className="text-xs text-gray-500">
                          {new Date(report.timestamp).toLocaleDateString('en-US', {
                            month: 'short',
                            day: 'numeric',
                            year: 'numeric'
                          })}
                        </span>
                        {report.verified ? (
                          <Badge variant="secondary" className="text-xs bg-green-100 text-green-800">
                            Verified
                          </Badge>
                        ) : (
                          <Badge variant="secondary" className="text-xs bg-yellow-100 text-yellow-800">
                            Pending
                          </Badge>
                        )}
                      </div>
                    </div>
                    <div className="text-right text-xs text-gray-500">
                      <div>{report.upvotes} upvotes</div>
                      {report.water_depth && (
                        <div className="capitalize">{report.water_depth}</div>
                      )}
                    </div>
                  </div>
                </div>
              ))}
              {userReports.length > 10 && (
                <div className="text-center text-sm text-gray-500 pt-2">
                  And {userReports.length - 10} more reports...
                </div>
              )}
            </div>
          ) : (
            <div className="text-center py-6 text-gray-500">
              <FileText className="w-12 h-12 mx-auto mb-2 opacity-50" />
              <p className="text-sm">No reports yet</p>
              <p className="text-xs mt-1">Submit your first flood report to help your community</p>
            </div>
          )}
        </Card>

        {/* Notification Preferences */}
        <Card className="p-4">
          <h3 className="font-semibold text-lg flex items-center gap-2 mb-4">
            <Bell className="w-5 h-5 text-gray-600" />
            Notification Preferences
          </h3>

          <div className="space-y-4">
            <div className="flex items-center justify-between">
              <Label htmlFor="push" className="cursor-pointer font-normal">
                Push notifications
              </Label>
              <Switch
                id="push"
                checked={user.notification_push ?? true}
                onCheckedChange={(checked) => handleNotificationToggle('notification_push', checked)}
              />
            </div>

            <div className="flex items-center justify-between">
              <Label htmlFor="sms" className="cursor-pointer font-normal">
                SMS alerts
              </Label>
              <Switch
                id="sms"
                checked={user.notification_sms ?? false}
                onCheckedChange={(checked) => handleNotificationToggle('notification_sms', checked)}
              />
            </div>

            <div className="flex items-center justify-between">
              <Label htmlFor="whatsapp" className="cursor-pointer font-normal">
                WhatsApp updates
              </Label>
              <Switch
                id="whatsapp"
                checked={user.notification_whatsapp ?? false}
                onCheckedChange={(checked) => handleNotificationToggle('notification_whatsapp', checked)}
              />
            </div>

            <div className="flex items-center justify-between">
              <Label htmlFor="email" className="cursor-pointer font-normal">
                Email notifications
              </Label>
              <Switch
                id="email"
                checked={user.notification_email ?? true}
                onCheckedChange={(checked) => handleNotificationToggle('notification_email', checked)}
              />
            </div>

            <Separator />

            <div>
              <Label className="mb-3 block font-semibold">Alert Types</Label>
              <div className="space-y-2">
                {[
                  { id: 'watch', label: 'Yellow Watch alerts', icon: 'üü°' },
                  { id: 'advisory', label: 'Orange Advisory', icon: 'üü†' },
                  { id: 'warning', label: 'Red Warning', icon: 'üî¥' },
                  { id: 'emergency', label: 'Emergency alerts', icon: '‚ö´' }
                ].map((alert) => (
                  <div key={alert.id} className="flex items-center gap-2">
                    <Checkbox
                      id={alert.id}
                      checked={user.alert_preferences?.[alert.id as keyof typeof user.alert_preferences] ?? true}
                      onCheckedChange={(checked) =>
                        handleAlertPreferenceToggle(alert.id, checked as boolean)
                      }
                    />
                    <Label htmlFor={alert.id} className="flex items-center gap-2 cursor-pointer font-normal">
                      <span>{alert.icon}</span>
                      <span className="text-sm">{alert.label}</span>
                    </Label>
                  </div>
                ))}
              </div>
            </div>
          </div>
        </Card>

        {/* Language Selection */}
        <Card className="p-4">
          <h3 className="font-semibold text-lg flex items-center gap-2 mb-4">
            <Globe className="w-5 h-5 text-gray-600" />
            Language
          </h3>

          <RadioGroup value={user.language || 'english'} onValueChange={handleLanguageChange}>
            <div className="flex items-center space-x-2">
              <RadioGroupItem value="english" id="english" />
              <Label htmlFor="english" className="cursor-pointer font-normal">English</Label>
            </div>
            <div className="flex items-center space-x-2">
              <RadioGroupItem value="hindi" id="hindi" />
              <Label htmlFor="hindi" className="cursor-pointer font-normal">‡§π‡§ø‡§®‡•ç‡§¶‡•Ä (Hindi)</Label>
            </div>
          </RadioGroup>
        </Card>

        {/* Privacy Settings */}
        <Card className="p-4">
          <h3 className="font-semibold text-lg flex items-center gap-2 mb-3">
            <Settings className="w-5 h-5 text-gray-600" />
            Privacy Settings
          </h3>

          <div className="space-y-4">
            <div className="flex items-center justify-between">
              <div>
                <Label>Show on Leaderboard</Label>
                <p className="text-xs text-gray-500">Display your profile on public leaderboards</p>
              </div>
              <Switch
                checked={user.leaderboard_visible !== false}
                onCheckedChange={(checked) => updateUserMutation.mutate({ leaderboard_visible: checked } as Partial<User>)}
              />
            </div>

            <Separator />

            <div className="flex items-center justify-between">
              <div>
                <Label>Public Profile</Label>
                <p className="text-xs text-gray-500">Allow others to view your profile</p>
              </div>
              <Switch
                checked={user.profile_public !== false}
                onCheckedChange={(checked) => updateUserMutation.mutate({ profile_public: checked } as Partial<User>)}
              />
            </div>

            <Separator />

            <div className="space-y-2">
              <Label>Display Name</Label>
              <p className="text-xs text-gray-500">Optional name shown instead of username</p>
              <Input
                placeholder="Enter display name"
                defaultValue={user.display_name || ''}
                onBlur={(e) => {
                  const value = e.target.value.trim();
                  if (value !== (user.display_name || '')) {
                    updateUserMutation.mutate({ display_name: value || null } as Partial<User>);
                  }
                }}
              />
            </div>
          </div>
        </Card>

        {/* About Section */}
        <Card className="p-4">
          <h3 className="font-semibold text-lg flex items-center gap-2 mb-3">
            <Settings className="w-5 h-5 text-gray-600" />
            About
          </h3>

          <div className="space-y-2 text-sm">
            <p className="text-gray-600">App version: 1.0.0 (MVP)</p>
            <Separator />
            <Button
              variant="ghost"
              className="w-full justify-start p-0 h-auto text-blue-600 font-normal"
              onClick={() => window.open('https://github.com/anthropics/floodsafe', '_blank')}
            >
              About FloodSafe
            </Button>
            <Button
              variant="ghost"
              className="w-full justify-start p-0 h-auto text-blue-600 font-normal"
              onClick={() => onNavigate?.('privacy')}
            >
              Privacy Policy
            </Button>
            <Button
              variant="ghost"
              className="w-full justify-start p-0 h-auto text-blue-600 font-normal"
              onClick={() => onNavigate?.('terms')}
            >
              Terms of Service
            </Button>
            <Button
              variant="ghost"
              className="w-full justify-start p-0 h-auto text-blue-600 font-normal"
              onClick={() => window.location.href = 'mailto:support@floodsafe.app?subject=FloodSafe Support Request'}
            >
              Contact Support
            </Button>
          </div>
        </Card>

        {/* Logout */}
        <Button
          variant="destructive"
          className="w-full"
          onClick={async () => {
            try {
              await logout();
              toast.success('Logged out successfully');
            } catch (error) {
              toast.error('Failed to logout');
            }
          }}
        >
          <LogOut className="w-4 h-4 mr-2" />
          Logout
        </Button>
      </div>
    </div>
  );
}

// Edit Profile Dialog Component
function EditProfileDialog({ user, onSave }: { user: User; onSave: (updates: Partial<User>) => void }) {
  const [username, setUsername] = useState(user.username);
  const [email, setEmail] = useState(user.email);
  const [phone, setPhone] = useState(user.phone || '');

  const handleSave = () => {
    const updates: Partial<User> = {};
    if (username !== user.username) updates.username = username;
    if (email !== user.email) updates.email = email;
    if (phone !== user.phone) updates.phone = phone;

    if (Object.keys(updates).length > 0) {
      onSave(updates);
    }
  };

  return (
    <DialogContent>
      <DialogHeader>
        <DialogTitle>Edit Profile</DialogTitle>
      </DialogHeader>
      <div className="space-y-4 py-4">
        <div className="space-y-2">
          <Label htmlFor="edit-username">Username</Label>
          <Input
            id="edit-username"
            value={username}
            onChange={(e) => setUsername(e.target.value)}
            placeholder="Enter username"
          />
        </div>
        <div className="space-y-2">
          <Label htmlFor="edit-email">Email</Label>
          <Input
            id="edit-email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            placeholder="Enter email"
          />
        </div>
        <div className="space-y-2">
          <Label htmlFor="edit-phone">Phone Number</Label>
          <Input
            id="edit-phone"
            type="tel"
            value={phone}
            onChange={(e) => setPhone(e.target.value)}
            placeholder="Enter phone number"
          />
        </div>
        <Button onClick={handleSave} className="w-full">
          Save Changes
        </Button>
      </div>
    </DialogContent>
  );
}
